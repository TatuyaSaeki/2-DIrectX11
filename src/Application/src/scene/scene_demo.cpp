//===========================================================================
//! @file	scene_demo.cpp
//!	@brief	デモシーン
//===========================================================================

//---------------------------------------------------------------------------
//! コンストラクタ
//---------------------------------------------------------------------------
SceneDemo::SceneDemo()
{
    sceneType_ = SceneType::Demo;
};

//---------------------------------------------------------------------------
//! 初期化
//---------------------------------------------------------------------------
bool SceneDemo::initialize()
{
    demoSceneManager_.reset(new DemoSceneManager());
    if(!demoSceneManager_)
        return false;
    if(!demoSceneManager_->initialize())
        return false;

    return true;
}

//---------------------------------------------------------------------------
//! 更新
//---------------------------------------------------------------------------
void SceneDemo::update(f32 deleteTime)
{
    demoSceneManager_->update(deleteTime);
}

//---------------------------------------------------------------------------
//! 描画
//---------------------------------------------------------------------------
void SceneDemo::render()
{
    demoSceneManager_->render();
}

//---------------------------------------------------------------------------
//! 描画(描画モード指定)
//---------------------------------------------------------------------------
void SceneDemo::render(RenderMode renderMode)
{
    demoSceneManager_->render(renderMode);
}

//---------------------------------------------------------------------------
//! 解放
//---------------------------------------------------------------------------
void SceneDemo::cleanup()
{
    demoSceneManager_->cleanup();
}

//---------------------------------------------------------------------------
//! ImGui描画
//---------------------------------------------------------------------------
void SceneDemo::showImGuiWindow()
{
    demoSceneManager_->showImGuiWindow();
}