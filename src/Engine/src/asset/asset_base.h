//===========================================================================
//! @file	asset_base.h
//!	@brief	アセット基底クラス
//===========================================================================
#pragma once

//===========================================================================
//! @clsss AssetBase
//===========================================================================
class AssetBase
{
public:
	//-----------------------------------------------------------------------
	//!	@name	初期化
	//-----------------------------------------------------------------------
	//@{

	//! @brief コンストラクタ
	AssetBase() = default;

	//! @brief デストラクタ
	virtual ~AssetBase() = default;

	//@}
	//-----------------------------------------------------------------------
	//!	@name	タスク
	//-----------------------------------------------------------------------
	//@{

	//-----------------------------------------------------------------------
	//! @brief 初期化
	//!	@param	[in]	filePath	ファイルパス
	//!	@return	true  正常終了
	//!	@return	false エラー終了
	//-----------------------------------------------------------------------
	virtual bool load(const std::string& filePath) = 0;

	//! @brief 解放
	virtual void cleanup() = 0;

	//@}

protected:
	std::string filePath_;	//!< ファイルパス保持用
};
