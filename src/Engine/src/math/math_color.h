//===========================================================================
//!	@file	math_color.h
//!	@brief	カラー
//===========================================================================
#pragma once

//===========================================================================
//!	@class	Color
//===========================================================================
struct Color
{
	//-----------------------------------------------------------------------
	//!	@name	初期化
	//-----------------------------------------------------------------------
	//{@

	//! デフォルトコンストラクタ
    Color() = default;

	//! コンストラクタ
    Color(u8 r, u8 g, u8 b, u8 a = 255);

	//! コンストラクタ
    Color(const Color& color);

	//@}
	//-----------------------------------------------------------------------
	//!	@name	オペレーター
	//-----------------------------------------------------------------------
	//@{

	//! operator=
    const Color& operator=(const Color& color);

	//@}

    union
    {
        struct
        {
            u32 r_ : 8;
            u32 g_ : 8;
            u32 b_ : 8;
            u32 a_ : 8;
        };
        u32 value_;
    };
};
