//---------------------------------------------------------------------------
//!	位置の設定
//---------------------------------------------------------------------------
void ObjectBase::setPosition(const Vector3& position)
{
    position_ = position;
}

//---------------------------------------------------------------------------
//!	位置の設定
//---------------------------------------------------------------------------
void ObjectBase::setPosition(f32 x, f32 y, f32 z)
{
    position_.x_ = x;
    position_.y_ = y;
    position_.z_ = z;
}

//---------------------------------------------------------------------------
//! 回転角の設定
//---------------------------------------------------------------------------
void ObjectBase::setRotation(const Vector3& rotation)
{
    Vector3 result = rotation;

    if(result.x_ > 360.0f || result.x_ < -360.0f)
        result.x_ = 0;
    if(result.y_ > 360.0f || result.y_ < -360.0f)
        result.y_ = 0;
    if(result.z_ > 360.0f || result.z_ < -360.0f)
        result.z_ = 0;

    rotation_ = result;
}

//---------------------------------------------------------------------------
//! 回転角の設定
//---------------------------------------------------------------------------
void ObjectBase::setRotation(f32 x, f32 y, f32 z)
{
    this->setRotation({ x, y, z });
}

