//===========================================================================
//! @file	manager_base.h
//!	@brief	マネージャーの基底クラス
//===========================================================================
#pragma once

//===========================================================================
//! @class ManagerBase
//===========================================================================
class ManagerBase
{
public:
	//-----------------------------------------------------------------------
    //! @name	初期化
	//-----------------------------------------------------------------------
    //@{

    //! @brief コンストラクタ
	ManagerBase() = default;

    //! @brief デストラクタ
    virtual ~ManagerBase() = default;

    //@}
	//-----------------------------------------------------------------------
	//! @name	タスク
	//-----------------------------------------------------------------------
	//@{

	//-----------------------------------------------------------------------
	//! @brief 初期化
	//! @return true  正常終了
	//! @return false エラー終了
	//-----------------------------------------------------------------------
	virtual bool initialize() { return true; }

	//! @brief 更新
	virtual void update() {};

	//! @brief 描画
	virtual void render() {};

	//-----------------------------------------------------------------------
	//! @brief 描画(描画モード指定)
	//! @param [in] renderMode 描画したいモード
	//-----------------------------------------------------------------------
    virtual void render([[maybe_unused]] RenderMode renderMode){};

	//! @brief 解放
	virtual void cleanup() {};

	//@}

private:
};
